/*!
    \brief      this function handles SysTick exception
    \param[in]  none
    \param[out] none
    \retval     none
*/
void SysTick_Handler(void)
{
  // delay_decrement();
	xTickCount++;
	if( xTickCount == ( TickType_t ) 0U )
	{
		Addmark++;
	}
}

//-------------hilink------------
unsigned long long Get_tick(void)
{
	return xTickCount;
}
