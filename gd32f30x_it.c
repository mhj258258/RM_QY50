typedef unsigned long long  TickType_t;
//--------hilink----------
static volatile TickType_t xTickCount = ( TickType_t ) 0U;
static volatile TickType_t Addmark = ( TickType_t ) 0U;



/*!
    \brief      this function handles NMI exception
    \param[in]  none
    \param[out] none
    \retval     none
*/
